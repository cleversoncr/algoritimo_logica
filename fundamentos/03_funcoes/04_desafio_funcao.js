// 2 parametros -->> console.log(+)
// 2 parametros -->> console.log(-)

function somar (a, b) {
  resultado = a + b;
  console.log(`Resultado da soma: ${resultado}`)
}

function subtrair (a, b) {
  resultado = a - b;
  console.log(`Resultado da subtração: ${resultado}`)
}

somar(3, 4)
somar(31, 90)

subtrair(11, 7)
subtrair(19, 52)